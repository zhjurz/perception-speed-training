export function getRandomWords(count, exclude = []) {
  const available = wordDatabase.words.filter((w) => !exclude.includes(w));
  const shuffled = [...available].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function getSynonyms(word) {
  return wordDatabase.synonyms[word] || [];
}

export function getSimilarShapeWords(word) {
  return wordDatabase.similarShapeWords[word] || [];
}

export function getDistractionWords(count, exclude = []) {
  const available = wordDatabase.distractionWords.filter(
    (w) => !exclude.includes(w),
  );
  const shuffled = [...available].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
